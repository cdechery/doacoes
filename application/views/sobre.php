<h2>O que é?</h2>
<p>O Interessa é um site para facilitar a doação, especialmente entre pessoa, mas também para instituições como ONGs, orfanatos, asilos, etc. Aqui você vai encontrar pessoas interessadas naquilo que você tem pra doar, e pode também achar instituições que estejam precisando. Você decide.</p>

<h3>Como funciona?</h3>

<div class="col roundbox">
	<h4>para pessoas:</h4>
	<p>Se você estiver procurando alguém interessado em algo que você não precisa ou usa mais, basta você se cadastrar, depois cadastrar os itens que tem aí sobrando e acumulando pó. No mapa você consegue filtrar as pessoas interessadas nas categorias de item que você cadastrou. Se elas tiverem 'Interesses' cadastrados, serão avisadas automaticamente.
</div>

<div class="col roundbox">
	<h4>para instituições:</h4>
	<p>Usuário cadastrados como Instituição não cadastram itens para doação. Apenas registram os interesses, indicando as categorias e distância máxima. Toda vez que um item novo for cadastrado naquela categoria, e dentro da distância escolhida, serão avisados, e podem entrar em contato com os doadores.	
</div>

<br clear="left">

<h3>Contato para doações</h3>
<p>O site atua apenas como um grande cadastro de pessoas e itens disponíveis para doação. Mas todo contato e negociação para realização das coletas e ou entrega dos itens, é feito diretamente entre as pessoas. Ao identificar um item que lhe interessa, você envia uma mensagem ao proprietário, se ele decidir responder, a partir desse momento toda a comunicação é feita diretamente por email.</p>